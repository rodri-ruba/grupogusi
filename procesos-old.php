                                                        <p class="c-font-17 c-margin-b-20 c-margin-t-20 c-font-thin "><strong>Rastro:</strong> Los cajones de noqueo, cadena de transportación, despielado y plataformas de trabajo se encuentran automatizados y sincronizados con la línea de producción, esto permite un mayor confort en el trabajo del personal del rastro y brinda a esta instalación de procesar hasta 1,200 animales por turno.</p>
                                                        <p class="c-font-17 c-margin-b-20 c-margin-t-20 c-font-thin "><strong>Vísceras:</strong> El área de vísceras cuenta con equipos de escaldado con tecnología francesa, permitiendo dar un acabado a las vísceras con aceptación internacional, adicionalmente el área cuenta con espacio exclusivo para empaque al vacío de víscera procesara y con túneles ráfaga de congelación, que permiten una congelación rápida de nuestros productos reduciendo así la merma en beneficio de nuestros clientes.</p>
